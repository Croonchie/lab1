// Step 2: Declare and Use Variables
let studentName = "Joshua Salazar";
let studentAge = 19;
let isEnrolled = true;

console.log("Student Name:", studentName);
console.log("Student Age:", studentAge);
console.log("Is Enrolled:", isEnrolled);

// Step 3: Demonstrate Data Types
// String
let myString = "Hello, World!";
console.log("String:", myString);

// Number
let myNumber = 3.14;
console.log("Number:", myNumber);

// Boolean
let myBoolean = false;
console.log("Boolean:", myBoolean);

// Array
let myArray = ["apple", "banana", "cherry"];
console.log("Array:", myArray);

// Object
let myObject = {
  firstName: "John",
  lastName: "Doe"
};
console.log("Object:", myObject);

// Step 4: Use Basic Operators
// Arithmetic Operators
console.log("Addition:", 5 + 3);
console.log("Subtraction:", 10 - 4);
console.log("Multiplication:", 6 * 7);
console.log("Division:", 20 / 5);

// Comparison Operators
console.log("Greater than:", 10 > 5);
console.log("Less than:", 2 < 8);
console.log("Equal to:", 4 === 4);
console.log("Not equal:", 9 !== 3);

// Logical Operators
console.log("AND (true && false):", true && false);
console.log("OR (true || false):", true || false);
console.log("NOT (!true):", !true);
